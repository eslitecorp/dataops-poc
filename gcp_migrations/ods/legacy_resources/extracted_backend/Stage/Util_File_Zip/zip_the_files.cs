        public void zipFileAndRemove(string filePath, string fileName, string zipFilePath)
        {
            try
            {
                if(File.Exists(zipFilePath))
                {
                    File.Delete(zipFilePath);
                }

                using(FileStream fs = new FileStream(filePath, FileMode.Open, FileAccess.Read))
                {
                    byte[] fByte = BinaryReadToEnd(fs); // 取得檔案byte[]

                    using (var zipFileStream = new FileStream(zipFilePath, FileMode.CreateNew))
                    {
                        using (var archive = new ZipArchive(zipFileStream, ZipArchiveMode.Create, true))
                        {
                            var zipArchiveEntry = archive.CreateEntry(fileName, CompressionLevel.Fastest);
                            BinaryReader bs = new BinaryReader(fs);

                            using (var zipStream = zipArchiveEntry.Open())
                            {
                                zipStream.Write(fByte, 0, fByte.Length);
                            }
                        }
                    }
                }

                File.Delete(filePath);
            } catch(Exception ex)
            {
                Dts.Log(ex.Message, 999, null);
            }
        }

        public byte[] BinaryReadToEnd(Stream stream)
        {
            long originalPosition = 0;

            if (stream.CanSeek)
            {
                originalPosition = stream.Position;
                stream.Position = 0;
            }

            try
            {
                byte[] readBuffer = new byte[4096];

                int totalBytesRead = 0;
                int bytesRead;

                while ((bytesRead = stream.Read(readBuffer, totalBytesRead, readBuffer.Length - totalBytesRead)) > 0)
                {
                    totalBytesRead += bytesRead;

                    if (totalBytesRead == readBuffer.Length)
                    {
                        int nextByte = stream.ReadByte();
                        if (nextByte != -1)
                        {
                            byte[] temp = new byte[readBuffer.Length * 2];
                            Buffer.BlockCopy(readBuffer, 0, temp, 0, readBuffer.Length);
                            Buffer.SetByte(temp, totalBytesRead, (byte)nextByte);
                            readBuffer = temp;
                            totalBytesRead++;
                        }
                    }
                }

                byte[] buffer = readBuffer;
                if (readBuffer.Length != totalBytesRead)
                {
                    buffer = new byte[totalBytesRead];
                    Buffer.BlockCopy(readBuffer, 0, buffer, 0, totalBytesRead);
                }
                return buffer;
            }
            finally
            {
                if (stream.CanSeek)
                {
                    stream.Position = originalPosition;
                }
            }
        }

